//Determina si un numero es mayor que 100.

let numero = prompt("Ingrese un número:");

// Verificar si el numero es mayor que 100
if (numero > 100) {
  console.log("El número es mayor que 100");
} else {
  // Si no se cumple la primera condicion se mostrara lo siguiente.
  console.log("El número no es mayor que 100");
}